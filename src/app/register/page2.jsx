            <li><strong>Oculogyral crisis/acute dystonic reaction</strong> i.e. extrapyramidal side effects (fixed gaze, agitated, neck spasms) e.g. Haloperidol. Usually within few hours of meds but may be after few days. Lifethreatening if laryngeal dystonia. Mx - antimuscarinics 1st line (e.g. benztropine, procyclidine) / benzos / antihistamines treat with Procyclidone</li>
